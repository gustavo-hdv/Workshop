    public void refreshView(String object, String like) {
        switch (object) {
            case "Clientes":
                tableView.searchViewClients(this, table, like);
                break;
            case "Funcionários":
                tableView.searchViewEmployees(this, table, like);
                break;
            case "Serviços":
                tableView.searchViewServices(this, table, like);
                break;
            case "Produtos":
                tableView.searchViewProducts(this, table, like);
                break;
            case "Usuários":
                tableView.searchViewUsers(this, table, like, this.userId, this.userAdmin);
                break;
            case "Veículos":
                tableView.searchViewVehicles(this, table, like);
                break;
            case "Orçamentos":
                tableView.searchViewOrcamentos(this, table, like);
                break;
            default:
                break;
        }
    }
    
